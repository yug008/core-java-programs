public class Main {
    public static void main(String[] args) {
        // ARITHMETIC AND ASSIGNMENT OPERATORS
        int a = 6;
        a += 7;
        System.out.println(a);

        int b = 90;
        b *= 67;
        System.out.println(b);

        double c = 7.9;
        c += 67.78;
        System.out.println(c);

        System.out.println(9>8);
        System.out.println(8.9>87.998);        //CONDITIONAL OPERATORS

        System.out.println(78>34);

        //LOGICAL OPERATORS
        System.out.println(45>32 && 65>87);
        System.out.println(90>78 && 54<56);
        System.out.println((87>900 && 67>68));

        System.out.println(55>76 || 45>9);
        System.out.println((45>87 || 87<78));

    }
}